def productAdjacent(input, digitCount):
    inputArray = [int(i) for i in str(input)] # change input into an array of ints
    maxProduct = -1
    for index in range(0, len(inputArray) - digitCount + 1):
        product = 1
        for digit in inputArray[index: index + digitCount]:
           if digit == 0:
               break
           else:
                product *= digit
        if product > maxProduct:
            maxProduct = product
    return maxProduct


